def add_note(doc, text):
    p = doc.add_paragraph()
    p.paragraph_format.left_indent = Cm(1)
    run = p.add_run(f'💡 {text}')
    run.italic = True
    run.font.color.rgb = RGBColor(0x2e, 0x7d, 0x32)

