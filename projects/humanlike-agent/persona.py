def build_system_prompt(mission: dict | None = None, chat_history_summary: str = "") -> str:
    """Собирает системный промпт."""
    prompt = DEFAULT_PERSONA + "\n" + ANTI_JAILBREAK

    if mission:
        prompt += f"\n\n## Твоя миссия в этом чате\n"
        prompt += f"Роль: {mission.get('mission', 'observer')}\n"
        if mission.get('persona'):
            prompt += f"Персонаж: {mission['persona']}\n"
        if mission.get('style'):
            prompt += f"Стиль: {mission['style']}\n"
        if mission.get('goals'):
            prompt += f"Цели: {', '.join(mission['goals'])}\n"
        if mission.get('triggers'):
            prompt += f"Триггеры (когда точно отвечать): {', '.join(mission['triggers'])}\n"

    if chat_history_summary:
        prompt += f"\n\n## Контекст чата\n{chat_history_summary}"

    return prompt


def format_chat_for_llm(messages: list[dict], agent_name: str = "Агент") -> list[dict]:
    """Форматирует историю чата для LLM."""
    llm_messages = []

    for msg in messages:
        if msg["is_agent"]:
            llm_messages.append({
                "role": "assistant",
                "content": msg["text"]
            })
        else:
            name = msg.get("first_name") or msg.get("username") or "Аноним"
            llm_messages.append({
                "role": "user",
                "content": f"[{name}]: {msg['text']}"
            })

    return llm_messages
